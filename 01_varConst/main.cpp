#include <iostream>

using namespace std;

int main()
{
    // Variables
    int score;   // declaration
    score = 500; // initialization
    score = 340;
    cout << score << endl;

    // Constant
    const int userId = 333223;
    // userId = 348; // Not Allow to change the value
    cout << userId << endl;

    cout << "Welcome to the learning of variables and constant in c++" << endl;
    return 0;
};